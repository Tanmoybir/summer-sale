function clickCard1(){
    const entry=document.getElementById('calculate');
    const cardName1=document.getElementById('card-1');
    const cardOne=cardName1.innerText;
    const li=document.createElement('li');
    li.innerText=cardOne;
    entry.appendChild(li);
}
